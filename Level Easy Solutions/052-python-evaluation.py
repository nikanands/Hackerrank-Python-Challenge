import random as r

l = ['apple', 'mango', 'orange', 'banana']

# Generate a random integer
print('Randint: ',r.randint(0,100))
print('\n', end='')
# Extended version of randint
print('Randrange: ',r.randrange(0, 100, 2))
print('\n', end='')
# Get random 'k' samples
print('Sample: ',r.sample(l, k=3))
print('\n', end='')
# Get random choice
print('Choice: ',r.choice(l))
print('\n', end='')
# Random shuffle
r.shuffle(l)
print(l)

